public class Prin {
	Prin() {
	}

	// Print method for String.
	static void t(String text) {
		System.out.print(text);
	}

	// Print line method for String.
	static void l(String text) {
		System.out.println(text);
	}

	// Print method for Integer.
	static void t(Integer number) {
		System.out.print(number);
	}

	// Print line method for Integer.
	static void l(Integer number) {
		System.out.println(number);
	}
}
